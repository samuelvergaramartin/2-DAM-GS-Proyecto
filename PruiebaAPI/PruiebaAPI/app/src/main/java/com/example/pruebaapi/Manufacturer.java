package com.example.pruebaapi;

public class Manufacturer {
    private Long id;
    private String name;
    private Integer numEmployees;
    private Integer year;

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public Integer getNumEmployees() {
        return numEmployees;
    }

    public Integer getYear() {
        return year;
    }
}
